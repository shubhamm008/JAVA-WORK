public class arrays1 {
    public static void main(String[] args)
    {
        int[] arr=new int[]{10,20,30,40,50};
        for(int i=0;i<5;i++)
        {
            System.out.println(arr[i]);
        }
        for(int x:arr)
        {
            System.out.println(x);
        }
    }

}
